-- ADDRESS SNAPSHOT DECISION:
--   Chosen: Inline columns on orders (prefixed snapshot_).
--   Rationale:
--     - A separate order_address_snapshots table would be a strict 1:1 with
--       orders, adding a JOIN for every order query with no normalisation gain
--       (the data is never shared across orders).
--     - Inline columns keep a complete order record in one row, which is
--       simpler, faster to query, and easier to audit.
--     - The snapshot prefix clearly communicates that these fields are
--       immutable point-in-time copies.
--   This decision is documented in docs/DATABASE_ARCHITECTURE.md.
